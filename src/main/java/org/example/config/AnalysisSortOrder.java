package org.example.config;
/**
 * Defines the sorting direction for analysis result sets.
 *
 * @author Denys Oleksiuk
 * @version 1.0.0 (2026-01-05)
 */
public enum AnalysisSortOrder {
    /** Ascending order (e.g., A-Z, 0-9, oldest to newest). */
    ASC,

    /** Descending order (e.g., Z-A, 9-0, newest to oldest). */
    DESC
}
